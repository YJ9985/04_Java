package afternoon.variable;

public class Var2Number {
    public static void main(String[] args) {
        //정수
        byte b = 127;
        short c = 32767;
        int i = 2147483647;
        long l = 9223372036854775807L; //L 표시하는게 관례

        //실수
        float f = 4.3f; //f 무조건 찍어야 함
        double d = 4.3; // double은 d 안 찍어도 됨
    }
}
