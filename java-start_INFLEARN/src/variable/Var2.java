package src.variable;

public class Var2 {
    public static void main(String[] args) {
        //1. 변수의 형식
        int a = 100; //정수형
        double b = 10.5; //실수형
        boolean c = true; //불리언
        char d = 'A'; //문자 하나
        String e = "Hello Java"; //문자열

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
